package thinking_test

import (
	"testing"

	"github.com/router-for-me/CLIProxyAPI/v6/internal/registry"
	"github.com/router-for-me/CLIProxyAPI/v6/internal/thinking"
	_ "github.com/router-for-me/CLIProxyAPI/v6/internal/thinking/provider/codex"
	"github.com/tidwall/gjson"
)

func TestParseSuffixForModel_HyphenThinkingLevel(t *testing.T) {
	got := thinking.ParseSuffixForModel("gpt-5.3-codex-spark-high", "codex")
	if !got.HasSuffix {
		t.Fatalf("expected hyphen suffix")
	}
	if got.ModelName != "gpt-5.3-codex-spark" || got.RawSuffix != "high" {
		t.Fatalf("ParseSuffixForModel() = %#v", got)
	}
}

func TestParseSuffixForModel_ExactHyphenModelWins(t *testing.T) {
	reg := registry.GetGlobalRegistry()
	reg.RegisterClient("thinking-exact-hyphen-test", "codex", []*registry.ModelInfo{
		{ID: "foo-high", Thinking: &registry.ThinkingSupport{Levels: []string{"low", "medium", "high", "xhigh"}}},
	})
	t.Cleanup(func() { reg.UnregisterClient("thinking-exact-hyphen-test") })

	got := thinking.ParseSuffixForModel("foo-high", "codex")
	if got.HasSuffix || got.ModelName != "foo-high" {
		t.Fatalf("exact model should not be parsed as suffix, got %#v", got)
	}
}

func TestParseSuffixForModel_AutoGeneratedThinkingAlias(t *testing.T) {
	reg := registry.GetGlobalRegistry()
	support := &registry.ThinkingSupport{Levels: []string{"low", "medium", "high", "xhigh"}}
	reg.RegisterClient("thinking-auto-alias-test", "codex", []*registry.ModelInfo{
		{ID: "foo", Thinking: support},
		{ID: "foo-high", Thinking: support, ThinkingAliasBase: "foo"},
	})
	t.Cleanup(func() { reg.UnregisterClient("thinking-auto-alias-test") })

	got := thinking.ParseSuffixForModel("foo-high", "codex")
	if !got.HasSuffix || got.ModelName != "foo" || got.RawSuffix != "high" {
		t.Fatalf("auto alias should parse to base+level, got %#v", got)
	}
}

func TestApplyThinking_CodexHyphenSuffixSetsReasoningEffort(t *testing.T) {
	body := []byte(`{"model":"gpt-5.3-codex-spark","reasoning":{"effort":"medium"}}`)
	out, err := thinking.ApplyThinking(body, "gpt-5.3-codex-spark-high", "openai", "codex", "codex")
	if err != nil {
		t.Fatalf("ApplyThinking() error = %v", err)
	}
	if got := gjson.GetBytes(out, "reasoning.effort").String(); got != "high" {
		t.Fatalf("reasoning.effort = %q, want high, body=%s", got, string(out))
	}
}
